    "            weight_sum = np.sum(weights)\n",
    "            if weight_sum > 0:\n",
    "                weights = weights / weight_sum\n",
    "                weighted_vec = np.sum(token_vecs * weights[:, np.newaxis], axis=0)\n",
    "            else:\n",
    "                weighted_vec = np.mean(token_vecs, axis=0)\n",
