    "# --- TF-IDF Weighted GloVe Pooling ---\n",
    "def compute_tfidf_weighted_glove(texts, tfidf_vec, glove_dict, dim=100):\n",
    "    tfidf_matrix = tfidf_vec.transform(texts).toarray()\n",
    "    feature_names = tfidf_vec.get_feature_names_out()\n",
    "    word2idx = {word: idx for idx, word in enumerate(feature_names)}\n",
    "    \n",
    "    vecs = []\n",
    "    for i, text in enumerate(texts):\n",
    "        tokens = text.split()\n",
    "        token_vecs = []\n",
    "        weights = []\n",
    "        for w in tokens:\n",
    "            if w in glove_dict:\n",
    "                token_vecs.append(glove_dict[w])\n",
    "                if w in word2idx:\n",
    "                    weights.append(tfidf_matrix[i, word2idx[w]])\n",
    "                else:\n",
    "                    weights.append(1e-3)\n",
    "        if token_vecs:\n",
    "            token_vecs = np.array(token_vecs)\n",
